import type { Employee } from "../backend.d";

const STORAGE_KEY = "salary_mgr_employees_data";

// Serializable format for localStorage (bigint as string)
type StoredEmployee = Omit<
  Employee,
  "basicSalary" | "allowances" | "joiningDate"
> & {
  basicSalary: string;
  allowances: string;
  joiningDate: string;
};

function toStored(e: Employee): StoredEmployee {
  return {
    ...e,
    basicSalary: e.basicSalary.toString(),
    allowances: e.allowances.toString(),
    joiningDate: e.joiningDate.toString(),
  };
}

function fromStored(s: StoredEmployee): Employee {
  return {
    ...s,
    basicSalary: BigInt(s.basicSalary),
    allowances: BigInt(s.allowances),
    joiningDate: BigInt(s.joiningDate),
  };
}

