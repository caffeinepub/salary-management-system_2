function isSeedLoaded(): boolean {
  return localStorage.getItem(`${STORAGE_KEY}_seeded`) === "1";
}

function loadSeed() {
  if (isSeedLoaded()) return;
  const existing = getRawEmployees();
  const existingIds = new Set(existing.map((e) => e.id));
  const toAdd = SEED_EMPLOYEES.filter((e) => !existingIds.has(e.id));
  if (toAdd.length > 0) {
    localStorage.setItem(STORAGE_KEY, JSON.stringify([...existing, ...toAdd]));
  }
  localStorage.setItem(`${STORAGE_KEY}_seeded`, "1");
}

function getRawEmployees(): StoredEmployee[] {
  try {
    return JSON.parse(localStorage.getItem(STORAGE_KEY) ?? "[]");
  } catch {
    return [];
  }
}

export function getAllEmployees(): Employee[] {
  loadSeed();
  return getRawEmployees().map(fromStored);
}

export function getEmployee(id: string): Employee | null {
  loadSeed();
  const found = getRawEmployees().find((e) => e.id === id);
  return found ? fromStored(found) : null;
}

export function saveEmployee(employee: Employee): void {
  loadSeed();
  const existing = getRawEmployees().filter((e) => e.id !== employee.id);
  localStorage.setItem(
    STORAGE_KEY,
    JSON.stringify([...existing, toStored(employee)]),
  );
}

export function deleteEmployee(id: string): void {
  const existing = getRawEmployees().filter((e) => e.id !== id);
  localStorage.setItem(STORAGE_KEY, JSON.stringify(existing));
}
